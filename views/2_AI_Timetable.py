# --- PDF ENGINE ---
def create_pdf_buffer(subject, plan):
    pdf = FPDF()
    pdf.add_page()
    
    # Border & Background (Light tint)
    pdf.set_fill_color(248, 250, 252)
    pdf.rect(5, 5, 200, 287, 'DF')
    pdf.set_draw_color(37, 99, 235)
    pdf.rect(7, 7, 196, 283) 

    # Header
    pdf.set_font("Helvetica", "B", 16)
    pdf.set_text_color(30, 58, 138)
    pdf.cell(190, 15, "M. KUMARASAMY COLLEGE OF ENGINEERING (Autonomous)", ln=True, align="C")
    
    pdf.set_font("Helvetica", "B", 12)
    pdf.set_text_color(100, 116, 139)
    pdf.cell(190, 10, "AI PROXY HUB: LESSON CONTINUITY PLAN", ln=True, align="C")
    pdf.ln(5)
    
    # Subject Banner
    pdf.set_fill_color(37, 99, 235)
    pdf.set_text_color(255, 255, 255)
    pdf.set_font("Helvetica", "B", 14)
    pdf.cell(190, 12, f" SUBJECT: {subject.upper()} ", ln=True, align="C", fill=True)
    pdf.ln(10)

    # Content
    pdf.set_text_color(0, 0, 0)
    pdf.set_font("Helvetica", "B", 12)
    pdf.cell(190, 10, "1. Executive Summary / Learning Objectives", ln=True)
    pdf.set_font("Helvetica", "", 11)
    pdf.multi_cell(180, 8, plan['summary'].encode('ascii', 'ignore').decode('ascii'))
    
    pdf.ln(10)
    pdf.set_font("Helvetica", "B", 12)
    pdf.cell(190, 10, "2. Verification Quiz (Post-Session Assessment)", ln=True)
    pdf.set_font("Helvetica", "", 11)
    for i, q in enumerate(plan['quiz'], 1):
        pdf.multi_cell(180, 8, f"{i}. {q.encode('ascii', 'ignore').decode('ascii')}")
    
    pdf.ln(15)
    pdf.set_font("Helvetica", "I", 9)
    pdf.set_text_color(148, 163, 184)
    pdf.cell(190, 10, f"Generated by MKCE Smart Classroom AI on {datetime.now().strftime('%Y-%m-%d %H:%M')}", ln=True, align="C")
    
    pdf_content = pdf.output()
    return BytesIO(pdf_content)

